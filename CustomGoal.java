/**
* Represents a user-defined custom goal created by the user.
*/
public class CustomGoal extends Goal{
	/** 
 	* Constructs a custom goal with a name and description.
     	* @param name the name of the goal
     	* @param description a brief explanation of the goal
  	*/
	public CustomGoal(String name, String description){
		super(name, description);
	}
}
